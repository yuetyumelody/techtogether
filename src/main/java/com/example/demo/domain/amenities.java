package com.example.demo.domain;

public class amenities {

    boolean elevator;
    boolean wheelchairRamps;
    boolean guideDogs;
    boolean wheelchairRental;
    boolean braille;
    boolean guidedTours;

    public amenities(boolean elevator, boolean wheelchairRamps, boolean guideDogs, boolean wheelchairRental, boolean braille,
            boolean guidedTours){

        this.elevator = elevator;
        this.wheelchairRamps = wheelchairRamps;
        this.guideDogs = guideDogs;
        this.wheelchairRental = wheelchairRental;
        this.braille = braille;
        this.guidedTours = guidedTours;


    }


}